public class MathOperations {
    // ví dụ nạp chồng phương thức
    // nạp chồng phương thức add với hai tham số kiểu int
    public int add (int a, int b) {
        return a + b;
    }

    // nạp choongf phương thức add với ba tham số kiểu int
    public int add (int a, int b, int c) {
        return a + b + c;
    }

    // nạp chồng phương thức add với 2 tham so kiểu double
    public double add (double a, double b) {
        return a + b;
    }




}
